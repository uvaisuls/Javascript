//Data types

//Primitive Datatypes

//String - contains only alphabetics
let str = "Uvais";
console.log(str);
console.log(typeof str);

//Number - Contains only numeric values
let nu = 23;
console.log(nu);
console.log(typeof nu);

//Boolean
let bo = true;
console.log(bo)
console.log(typeof bo);

//null
let nul = null;
console.log(nul);
console.log(typeof nul);

//undefined
let und;
console.log(und);
console.log(typeof und);

//Reference Datatype

//Array
let arr = ["apple",1,"banana",2];
console.log(arr);
console.log(typeof arr);

//Object
let obj = {
    name: "Uvais",
    age: 20,
    matrialStatus: "Unmarried" 
};
console.log(obj);
console.log(typeof obj);